// url: https://leetcode.com/problems/interval-cancellation/
// given a function, fn; an array of arguments, args; an interval time in ms, t
// return a cancel function, cancelFn
// when the cancel function gets called
/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
function cancellable(fn, args, t){
    // immediately, then at intervals
    const call = () => fn(...args);
    call();
    const intervalID = setInterval(call, t);
    // make a helper function
    return () => clearInterval(intervalID);
    // return cancel function
};

